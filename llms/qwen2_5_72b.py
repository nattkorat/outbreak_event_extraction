from utils import openrouter

def chat_with_qwen2_5_72b(text: str) -> str:
    """
    Function to interact with Qwen 2.5 72B model for text generation.
    
    Args:
        text (str): The input text to be processed by the model.
        
    Returns:
        str: The response generated by the model.
    """
    response = openrouter.client.chat.completions.create(
        model="qwen/qwen-2.5-72b-instruct:free",
        messages=[
            {
              "role": "system",
              "content": "You are a helpful assistant that always answer as JSON."
            },
            {
                "role": "user",
                "content": text
            }
        ]
    )
    if not response:
        return "Return an empty string from the model."
    
    return response.choices[0].message.content

if __name__ == "__main__":
  # Example usage
  example_text = "What is the capital of France?"
  response = chat_with_qwen2_5_72b(example_text)
  print(f"Response from Qwen 2.5: {response}")